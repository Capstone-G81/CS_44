package com.pojo;
public class Teacher
{
 
	public Teacher(){
  
	}
	 
	private Long id;
        
         
	private String tno;
        
         
	private String name;
        
         
	private String sex;
        
         
	private String tel;
        
         
	private String zc;
        
         
	private String pwd;
        
        
         
	public Long getId(){
		return this.id;
	}
 
	public void setId(Long id){
		this.id = id;
	}
		
         
	public String getTno(){
		return this.tno;
	}
 
	public void setTno(String tno){
		this.tno = tno;
	}
		
         
	public String getName(){
		return this.name;
	}
 
	public void setName(String name){
		this.name = name;
	}
		
         
	public String getSex(){
		return this.sex;
	}
 
	public void setSex(String sex){
		this.sex = sex;
	}
		
         
	public String getTel(){
		return this.tel;
	}
 
	public void setTel(String tel){
		this.tel = tel;
	}
		
         
	public String getZc(){
		return this.zc;
	}
 
	public void setZc(String zc){
		this.zc = zc;
	}
		
         
	public String getPwd(){
		return this.pwd;
	}
 
	public void setPwd(String pwd){
		this.pwd = pwd;
	}
		
            
}
